#configure_systemd_timer() {  log_info "Configuring systemd timer for monitoring..."    
# Copy templates  local template_service="${INSTALLER_ROOT}/templates/systemd/ydea-ticket-monitor.service"  local template_timer="${INSTALLER_ROOT}/templates/systemd/ydea-ticket-monitor.timer"    if [[ -f "$template_service" ]]; then    cp "$template_service" /etc/systemd/system/  else    
# Create service unit    cat > /etc/systemd/system/ydea-ticket-monitor.service <<EOF[Unit]Description=Ydea Ticket MonitorAfter=network.target[Service]Type=oneshotExecStart=$YDEA_INSTALL_DIR/ydea-ticket-monitor.shStandardOutput=journalStandardError=journalEOF  fi    if [[ -f "$template_timer" ]]; then    cp "$template_timer" /etc/systemd/system/  else    
# Create timer unit    cat > /etc/systemd/system/ydea-ticket-monitor.timer <<EOF[Unit]Description=Ydea Ticket Monitor TimerRequires=ydea-ticket-monitor.service[Timer]OnBootSec=5minOnUnitActiveSec=${YDEA_MONITOR_INTERVAL:-30}minAccuracySec=1min[Install]WantedBy=timers.targetEOF  fi    
# Enable and start timer  log_command "systemctl daemon-reload"  log_command "systemctl enable ydea-ticket-monitor.timer"  log_command "systemctl start ydea-ticket-monitor.timer"    log_success "Systemd timer configured (runs every ${YDEA_MONITOR_INTERVAL:-30} minutes)"}
