#deploy_check_scripts() {  local platform="$1"    log_info "Deploying $platform check scripts..."    local src="${SCRIPTS_SRC}/script-check-${platform}"  local dest="/opt/script-check-${platform}"    if [[ ! -d "$src" ]]; then    log_warning "Check scripts for $platform not found"    return 0  fi    
