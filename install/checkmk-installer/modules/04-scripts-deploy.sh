#configure_checkmk_notifications() {  log_info "Configuring CheckMK notification scripts..."    local site_name="${CHECKMK_SITE_NAME:-monitoring}"  local omd_root="/omd/sites/$site_name"  local notify_dir="$omd_root/local/share/check_mk/notifications"    if [[ ! -d "$omd_root" ]]; then    log_warning "CheckMK site not found, skipping notification configuration"    return 0  fi    
