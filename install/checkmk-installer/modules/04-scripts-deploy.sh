#detect_os_type() {  local check_type="$1"    case "$check_type" in    "ns7")      
# Check for NethServer 7      if [[ -f /etc/nethserver-release ]]; then        return 0      fi      ;;    "ns8")      
# Check for NethServer 8 (has runagent command)      if command -v runagent &>/dev/null; then        return 0      fi      ;;    "ubuntu")      
# Check for Ubuntu/Debian      if [[ -f /etc/os-release ]]; then        source /etc/os-release        if [[ "$ID" == "ubuntu" ]] || [[ "$ID" == "debian" ]]; then          return 0        fi      fi      ;;    "proxmox")      
# Check for Proxmox VE      if [[ -f /etc/pve/.version ]]; then        return 0      fi      ;;    "windows")      
# Windows detection (usually not run from bash)      if [[ "$OSTYPE" == "msys" ]] || [[ "$OSTYPE" == "cygwin" ]]; then        return 0      fi      ;;  esac    return 1}
